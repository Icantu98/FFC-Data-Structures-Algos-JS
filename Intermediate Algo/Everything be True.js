function truthCheck(collection, pre) {
	let count = 0 // to count up all the truthys in array of objs. Then at the end check if this matches the length of the array. If true then all are Truthy
	for (let obj in collection){
		if(collection[obj].hasOwnProperty(pre) && Boolean(collection[obj][pre])){
			count ++
		}
	}
	return count == collection.length
}
  
